    print("Minimum za ", godina ,"je", min(lista_koeficijenata))
    print("Maksimum za ", godina, "je:", max(lista_koeficijenata))
    print("Prosek za ", godina, '-u. je:', sum(lista_koeficijenata)/len(lista_koeficijenata))

