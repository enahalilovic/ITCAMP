def formatiranje_sadrzaja(path):  # argument je putanja do fajla koji cemo da otvorimo
    f = open(path)
    sadrzaj_string = f.read() # otvaramo ceo sadrzaj u stringu
    f.close()

    lista_redova = sadrzaj_string.split('\n') # pravimo listu u kojoj je svaki red jedan string

    for i in range(len(lista_redova)):
        lista_redova[i] = lista_redova[i].split(',')   # listu delimo na podlistu, svaki element je string, delimo na mestu gde su zarezi 
    
    sadrzaj_recnik = {}
    for i in lista_redova:
        if i[0] == 'CO2 per capita':
            sadrzaj_recnik[i[0]] = [int(x) for x in i[1:]]     # ime drzave je kljuc u intu 
        else:
            sadrzaj_recnik[i[0]] = [float(x) for x in  i[1:]]  # vrednosti su float 
    return sadrzaj_recnik

